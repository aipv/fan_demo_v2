void test_pipe_mfcc_float()
{
    float output[13];
    dsp_mfcc_counter_t *stat;

    dsp_mfcc_init();
    dsp_pipe_init_statistic(1);
    dsp_pipe_mfcc_float(test_pcm16_512, 512, output);
    dsp_pipe_get_statistic(&stat);

    for (int i = 0; i < 13; i++)
    {
        printf ("%.08f, ", output[i]);
    }
    printf("\n-----------------------------------\n");

    printf("Stat of %d frame : \n", stat->n_frame);
    for (int i = 0; i < 13; i++)
    {
        printf ("%.08f, %.08f, %.08f, %.08f\n", stat->counter[i].min, stat->counter[i].max, stat->counter[i].sum, stat->counter[i].sum2);
    }
}

#define PCM_SAMPLES 16000
#define DSP_COEFS 1274
#define PCM_INPUT_FILE      "../data/input_pcm.bin"
#define DSP_OUTPUT_FILE      "../data/output_dsp.bin"

static float scaling_factor[DSP_MFCC_COEF_SIZE] = {1.0751028, 6.46053678, 9.90142454, 10.36176891, 13.33033384, 12.72560352, 13.14793948, 22.90498167, 20.47576543, 21.30183981, 20.23153021, 25.02760048, 20.26385331};

static int load_pcm(const char *path,int16_t *pcm)
{
    FILE *fp=fopen(path,"rb");
    if(!fp) return -1;
    size_t n=fread(pcm,sizeof(int16_t),PCM_SAMPLES,fp);
    fclose(fp);
    return n==PCM_SAMPLES?0:-1;
}

static int load_dsp(const char *path,int8_t *dsp)
{
    FILE *fp=fopen(path,"rb");
    if(!fp) return -1;
    size_t n=fread(dsp,sizeof(int8_t),DSP_COEFS,fp);
    fclose(fp);
    return n==DSP_COEFS?0:-1;
}

void test_dsp_pipe_mfcc_int8()
{
    int16_t pcm[PCM_SAMPLES];
    int8_t expect[DSP_COEFS];
    int8_t dsp[DSP_COEFS];

    load_pcm(PCM_INPUT_FILE, pcm);
    load_dsp(DSP_OUTPUT_FILE, expect);

    dsp_pipeline_init(scaling_factor);
    dsp_pipe_mfcc_int8(pcm, PCM_SAMPLES, dsp);
    int ret = memcmp(expect, dsp, DSP_COEFS);
    printf("test_dsp_pipe_mfcc_int8 : %d\n", ret);
}

int main()
{
    test_pipe_mfcc_float();
    test_dsp_pipe_mfcc_int8();
}

